export default class StringSchema extends AnySchema<string> {
  constructor() {
    super('string');
  }

  protected check(value: unknown): value is string {
    return Utils.isString(value);
  }

  protected coerce(value: unknown) {
    return String(value);
  }

  public length(length: number, message?: string) {
    this.addRule({
      type: 'length',
      message,
      validate: ({ value }) => {
        if (!Utils.isNumber(length))
          throw new LyraError('The parameter length for string.length must be a number');

        return value.length === length;
      },
    });

    return this;
  }

  public min(length: number, message?: string) {
    this.addRule({
      type: 'min',
      message,
      validate: ({ value }) => {
        if (!Utils.isNumber(length))
          throw new LyraError('The parameter length for string.min must be a number');

        return value.length >= length;
      },
    });

    return this;
  }

  public max(length: number, message?: string) {
    this.addRule({
      type: 'max',
      message,
      validate: ({ value }) => {
        if (!Utils.isNumber(length))
          throw new LyraError('The parameter length for string.max must be a number');

        return value.length <= length;
      },
    });

    return this;
  }

  public test(regex: RegExp, message?: string) {
    this._test(regex, 'test', message);

    return this;
  }

  public email(message?: string) {
    this._test(emailRegex, 'email', message);

    return this;
  }

  public url(message?: string) {
    this._test(urlRegex, 'url', message);
  }

  private _test(regex: RegExp, type: string, message?: string) {
    this.addRule({
      type: 'test',
      message,
      validate: ({ value }) => {
        if (!Utils.instanceOf(regex, RegExp))
          throw new LyraError('The parameter regex for string.test must be an instance of RegExp');

        return regex.test(value);
      },
    });
  }
}
